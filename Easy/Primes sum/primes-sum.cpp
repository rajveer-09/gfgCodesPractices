class Solution {
public:
bool isPrime(int num) {
    if (num <= 1) {
        return false;
    }
    if (num <= 3) {
        return true;
    }
    if (num % 2 == 0 || num % 3 == 0) {
        return false;
    }
    for (int i = 5; i * i <= num; i += 6) {
        if (num % i == 0 || num % (i + 2) == 0) {
            return false;
        }
    }
    return true;
}
    
    string isSumOfTwo(int N){
        // code here
        if (N <= 1) {
        return "No";
    }
    
    for (int i = 2; i <= N / 2; i++) {
        if (isPrime(i) && isPrime(N - i)) {
            return "Yes";
        }
    }
    
    return "No";


    }
};

